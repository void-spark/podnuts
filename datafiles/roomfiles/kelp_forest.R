Upon leaving the gentle waters of the ocean cove, you enter a dense kelp
forest. You can feel the slick rubbery plants caress every inch of your
body as the motion of the sea slowly moves the kelp back and forth.  To
stop swimming means becoming perfectly neutrally buoyant... embraced and
supported by the myriad of tall stalks that surround you.  Life is
abundant here, but the sense of security and privacy makes you feel as
though there is nothing to fear.  A territorial clown fish gently nips you
back to reality as he defends his would-be home.

