A large ocean swell carries you out still farther into the open ocean.
Sight of land has been masked by the curvature of the planet and the sea
floor has long since become an unreachable place.  Although nothing can be
seen, the eerie sound of whalesong, the identification whistles of
dolphins, and the occasional popping sound made by large shrimp can be
heard.  The sky above is of the deepest blue and the sun sinking further
near the horizon casts a calming violet hue across the entire expanse of
the sky.
