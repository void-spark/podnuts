Before you is a barrier reef.  This reef has grown to be quite large and
is the anchor by which its rich and abundant community has grown.  The
coral feeds at night on the rich abundance of zooplankton while the
community that the coral supports lives and feeds during the day.  Huge
schools of silver sided fish dart and turn in unison at the sight of you
while several large tangs, angelfish, and parrotfish bravely approach
taking test *NIPS* at you to determine if you're edible or not.  Crab.
lobster, and several cuttle fish all go about their daily lives taking
brief and fleeting glances at you.
